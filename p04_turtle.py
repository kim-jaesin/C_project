import turtle # 모듈

t=turtle.Turtle()
t.shape("arrow")#arrow,circie,aquare,

t.forward(200)#t(Turtle)를(.) 앞으로(forward) 200을 간다.
t.left(90)#t(Turtle)를(left)왼쪽으로 90도로 꺽는다.
t.forward(50)##t(Turtle)를(.) 앞으로(forward) 50을 간다.

turtle.mainloop()
turtle.bye()